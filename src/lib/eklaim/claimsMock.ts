/** Lookup helper — return claim by exact id atau undefined. */
export function findClaim(id: string): ClaimRecord | undefined {
  return CLAIM_BOARD_MOCK.find((claim) => claim.id === id);
}

/** Lookup helper — return claims by status. */
export function findClaimsByStatus(status: ClaimStatus): ReadonlyArray<ClaimRecord> {
  return CLAIM_BOARD_MOCK.filter((claim) => claim.statusPenjamin === status);
}

/** Lookup helper — return claims by penjamin type. */
export function findClaimsByPenjamin(tipe: TipePenjamin): ReadonlyArray<ClaimRecord> {
  return CLAIM_BOARD_MOCK.filter((claim) => claim.penjamin.tipe === tipe);
}
